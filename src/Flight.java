import java.time.LocalDate;

public class Flight {
	private LocalDate departure;
	private LocalDate arrival;
	private String location;
	private int price;
	
	public Flight(
			LocalDate departure,
			LocalDate arrival,
			String location,
			int price) {
		departure = this.departure;
		arrival = this.arrival;
		location = this.location;
		price = this.price;
		
	}
	

}
